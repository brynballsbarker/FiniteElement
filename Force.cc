//---------------------------------------------------------------------------//
// Constructor.
Force::Force( const int f_order,
              const double c )
    : d_f_order( f_order )
    , d_c( c )
{ 
    

}

//---------------------------------------------------------------------------//
// Get the value of f at a point x.
double Force::evaluate( const double& coord ) const
{
    if ( d_f_order == 0 )
        return  d_c;
    else if (d_f_order == 1 )
        return coord;
    else
       return coord * coord;
}

